# LOOPs ---> for loop, while loop
# Repeat a block of code multiple times

# range(start, stop. step)
# syntax
# for loop

for i in range(5):   # range from 0 to 4
    print(i)         # i is just variable, it can be anything instead of i

print("---------------------------------------")

for i in range(1,5):   # 1 to 4
    print(i)

print("---------------------------------------")

for i in range(3,5):   # 3 to 4
    print(i)

# Range (start, stop, step

print("---------------------------------------")

for i in range(1, 10, 1):   # 1 to 9, # range(start, stop. step)
    print(i)

print("---------------------------------------")

for i in range(1, 10, 2):   # 1 to 9
    print(i)

print("---------------------------------------")

for i in range(1, 10, 3):   # 1 to 9
    print(i)


