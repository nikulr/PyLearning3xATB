# Dynamically Typed
# Dynamic Type - Type of data that python supports.

age =65
# variableName = VariableValue
# identifier = literal

# Types which are supported in python
# Integers - Positive and negative whole numbers. : 1, 22, *12, 555
# Floating Points Numbers : 10.5, 55.12,  212.1
# Strings: " Hello", 'Hi'. '''Rocky Handsome'''
name = "Rocky Handsome"

# Booleans " True, False
isMale= True

# How to check the type of variable
print(type(age))
