# Dynamically Typed
# Dynamic Type - Type of data that python supports.

# Variable:

# variableName = VariableValue
# identifier = literal       ---> identifier means in simple word name
# A variable is a container(storage area) used to hold data

age = 65
print(type(age))

age = "sixty five"
print(type(age))

# Rule to crate Variable name:
# 1. Variable name should start from A to Z or a to z

# 2. Variable name should not start with number  ---> 123 = 345/Rocky  ---> variable name

# 3. should not be any keyword    --> keywords means Reserved words in Python
# Keywords in Python include:
# False, True, None, and, as, assert, async, await, def, del, elif, else, break, class,
# continue, except, finally, for, from, global, if, import, in, is, lambda, nonlocal, not, or, pass, raise,
# return, try, while, with and yield.

# 4. Variable name should not start with any special char except "_"
#  eg:  _age, _Name

